    /// <summary>
    /// Interaction logic for EssantialWindowsButtons.xaml
    /// </summary>
    public partial class EssentialWindowsButtons : UserControl
    {
        public EssentialWindowsButtons()
        {
            InitializeComponent();
        }

        private void MinimizeButton_Click(object sender, RoutedEventArgs e)
        {
            var window = Window.GetWindow(this);
            if (window != null)
            {
                window.WindowState = WindowState.Minimized;
            }
        }

        private void CloseButton_Click(object sender, RoutedEventArgs e)
        {
            var window = Window.GetWindow(this);
            if (window != null)
            {
                window.Close();
            }
        }
    }
}
